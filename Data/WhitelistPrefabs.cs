    public static bool IsAllowed(int guidHash)
    {
        return AllowedPrefabs.ContainsKey(guidHash);
    }

    public static string GetName(int guidHash)
    {
        if (AllowedPrefabs.TryGetValue(guidHash, out string name))
        {
            return name;
        }
        return "Unknown_Prefab";
    }

    public static bool TryGetHash(string input, out int guidHash)
    {
        if (int.TryParse(input, out guidHash))
        {
            return AllowedPrefabs.ContainsKey(guidHash);
        }

        foreach (var kvp in AllowedPrefabs)
        {
            if (string.Equals(kvp.Value, input, System.StringComparison.OrdinalIgnoreCase) ||
                string.Equals(kvp.Value.Substring(5), input, System.StringComparison.OrdinalIgnoreCase)) 
            {
                guidHash = kvp.Key;
                return true;
            }
        }

        guidHash = 0;
        return false;
    }
